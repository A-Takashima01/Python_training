# Comparison Operators
# Always returns true or false
# Equal
print(5 == 5)
# Not Equal
print(5 != 3)
# Greater Than
print(5 > 3)
# Less Than
print(3 < 5)
# Greater Than or Equal to
print(5 >= 5)
# Less Than or Equal to
print(3 <= 5)

# Logical Operators
# and -> True (Both True)
print(True and True)
# or -> True (Any True)
print(True or False)
# not -> Opposite
print(not True)

